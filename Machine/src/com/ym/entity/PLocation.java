package com.ym.entity;

/**
 * PLocation entity. @author MyEclipse Persistence Tools
 */

public class PLocation implements java.io.Serializable {

	// Fields

	private String id;
	private String name;
	private String hardwareversion;
	private String softwareversion;
	private String outroomdate;
	private String createusername;
	private String createtime;

	// Constructors

	/** default constructor */
	public PLocation() {
	}

	/** minimal constructor */
	public PLocation(String id) {
		this.id = id;
	}

	/** full constructor */
	public PLocation(String id, String name, String hardwareversion,
			String softwareversion, String outroomdate, String createusername,
			String createtime) {
		this.id = id;
		this.name = name;
		this.hardwareversion = hardwareversion;
		this.softwareversion = softwareversion;
		this.outroomdate = outroomdate;
		this.createusername = createusername;
		this.createtime = createtime;
	}

	// Property accessors

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHardwareversion() {
		return this.hardwareversion;
	}

	public void setHardwareversion(String hardwareversion) {
		this.hardwareversion = hardwareversion;
	}

	public String getSoftwareversion() {
		return this.softwareversion;
	}

	public void setSoftwareversion(String softwareversion) {
		this.softwareversion = softwareversion;
	}

	public String getOutroomdate() {
		return this.outroomdate;
	}

	public void setOutroomdate(String outroomdate) {
		this.outroomdate = outroomdate;
	}

	public String getCreateusername() {
		return this.createusername;
	}

	public void setCreateusername(String createusername) {
		this.createusername = createusername;
	}

	public String getCreatetime() {
		return this.createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

}